import React, { useRef } from "react";

function App() {
  const aboutRef = useRef(null); // Create a reference

  const scrollToAbout = () => {
    // Scroll to the element using the reference
    aboutRef.current?.scrollIntoView({ behavior: "smooth" });
  };

  return (
    <div>
      <button onClick={scrollToAbout}>About</button>

      <div style={{ height: "100vh" }} /> {/* To create scroll space */}

      <section ref={aboutRef}>
        <h2>About Section</h2>
        <p>This is the About section content.</p>
      </section>
    </div>
  );
}

export default App;
